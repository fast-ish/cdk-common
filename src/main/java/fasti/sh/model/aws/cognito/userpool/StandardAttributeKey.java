/**
 * Enumeration of standard attribute keys for Cognito User Pool attributes.
 *
 * <p>
 * Defines the standard OIDC claims and attributes that are supported by Cognito User Pools for user profiles.
 */
public enum StandardAttributeKey {
  address, birthdate, email, familyName, fullname, gender, givenName, lastUpdateTime, locale, middleName, nickname, phoneNumber, preferredUsername, profilePage, profilePicture, timezone, website
}
